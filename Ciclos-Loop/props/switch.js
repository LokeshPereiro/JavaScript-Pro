const dia = 2; //Domingo

// ===, es igual que, SWITCH cuando queremos igual a un valor único

switch (dia) {
    case 0:
        console.log('Domingo');
        break;
    case 1:
        console.log('Sabado');
        break;
    case 2:
        console.log('Lunes');
        break;
    default:
        console.log('No se qué dia es....');
}